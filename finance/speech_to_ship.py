    str = str.replace("hai", "2")
    str = str.replace("ba", "3")
    str = str.replace("bốn", "4")
    str = str.replace("năm", "5")
    str = str.replace("sáu", "6")
    str = str.replace("bảy", "7")
    str = str.replace("tám", "8")
    str = str.replace("chín", "9")
    str = str.replace("không", "0")

    result = str.replace(",", "*")
    return result


